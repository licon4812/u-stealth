namespace WinUI.TableView;

/// <summary>
/// Specifies the mode of the corner button in a TableView.
/// </summary>
public enum TableViewCornerButtonMode
{
    /// <summary>
    /// No button.
    /// </summary>
    None,

    /// <summary>
    /// Show Select All button.
    /// </summary>
    SelectAll,

    /// <summary>
    /// Show Options button.
    /// </summary>
    Options
}
